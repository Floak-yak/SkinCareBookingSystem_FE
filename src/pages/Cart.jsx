import React, { useContext } from "react";
import { useNavigate } from "react-router-dom";
import { List, Button, message } from "antd";
import CartContext from "../context/CartContext";
import useAuth from "../hooks/useAuth";
import productApi from "../api/productApi";
import "../styles/cart.css";

const Cart = () => {
  const navigate = useNavigate();
  const { cart, dispatch } = useContext(CartContext);
  const { user } = useAuth(); 
  console.log("User t·ª´ useAuth:", user);

  // Gi·∫£m s·ªë l∆∞·ª£ng
  const handleDecrease = (e, id, currentQty) => {
    e.stopPropagation();
    if (currentQty > 1) {
      dispatch({ type: "UPDATE_ITEM", payload: { id, quantity: currentQty - 1 } });
    } else {
      dispatch({ type: "REMOVE_ITEM", payload: { id } });
    }
  };

  // TƒÉng s·ªë l∆∞·ª£ng
  const handleIncrease = (e, id, currentQty) => {
    e.stopPropagation();
    dispatch({ type: "UPDATE_ITEM", payload: { id, quantity: currentQty + 1 } });
  };

  // X√≥a s·∫£n ph·∫©m
  const handleRemove = (e, id) => {
    e.stopPropagation();
    dispatch({ type: "REMOVE_ITEM", payload: { id } });
  };

  // Khi click v√†o s·∫£n ph·∫©m => xem chi ti·∫øt
  const handleItemClick = (id) => {
    navigate(`/products/${id}`);
  };

  // T√≠nh t·ªïng s·ªë ti·ªÅn
  const totalPrice = cart.items.reduce(
    (acc, item) => acc + item.price * item.quantity,
    0
  );

  // üü¢ X·ª≠ l√Ω khi b·∫•m n√∫t "Thanh to√°n"
  const handleCheckout = async () => {
    if (!user || !user.userId) {
      message.error("B·∫°n c·∫ßn ƒëƒÉng nh·∫≠p tr∆∞·ªõc khi thanh to√°n!");
      return;
    }
  
    const checkoutProductInformation = cart.items.map((item) => ({
      id: item.id,
      amount: item.quantity,
    }));
  
    const checkoutData = {
      userId: user.userId, // üü¢ L·∫•y userId t·ª´ useAuth()
      checkoutProductInformation,
    };
  
    console.log("Checkout Data g·ª≠i l√™n:", checkoutData); // Debug d·ªØ li·ªáu g·ª≠i l√™n BE
  
    try {
      const res = await productApi.checkOut(checkoutData);
      console.log("‚úÖ Thanh to√°n th√†nh c√¥ng:", res.data);
  
      message.success("Thanh to√°n th√†nh c√¥ng!");
      dispatch({ type: "CLEAR_CART" });
      navigate("/thank-you");
    } catch (err) {
      console.error("‚ùå L·ªói thanh to√°n:", err.response?.data || err.message);
      message.error("Thanh to√°n th·∫•t b·∫°i!");
    }
  };

  return (
    <div className="cart-container">
      <h2>Gi·ªè h√†ng c·ªßa b·∫°n</h2>

      {cart.items.length === 0 ? (
        <p>Gi·ªè h√†ng tr·ªëng.</p>
      ) : (
        <>
          <List
            itemLayout="horizontal"
            dataSource={cart.items}
            renderItem={(item) => (
              <List.Item
                onClick={() => handleItemClick(item.id)}
                actions={[
                  <Button onClick={(e) => handleDecrease(e, item.id, item.quantity)}>-</Button>,
                  <span style={{ width: 30, textAlign: "center" }}>{item.quantity}</span>,
                  <Button onClick={(e) => handleIncrease(e, item.id, item.quantity)}>+</Button>,
                  <Button danger onClick={(e) => handleRemove(e, item.id)}>X√≥a</Button>,
                ]}
              >
                <List.Item.Meta
                  avatar={<img src={item.image} alt={item.name} />}
                  title={item.name}
                  description={`${item.price.toLocaleString()} VND`}
                />
              </List.Item>
            )}
          />

          <div className="cart-footer">
            <div className="cart-total">
              <span>T·ªïng ti·ªÅn: </span>
              <strong>{totalPrice.toLocaleString()} VND</strong>
            </div>
            <Button type="primary" onClick={handleCheckout}>
              Thanh to√°n
            </Button>
          </div>
        </>
      )}
    </div>
  );
};

export default Cart;
